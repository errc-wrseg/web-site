       <li>
        "On Writing Well", escrito por William Zinsser, &eacute; um dos melhores e mais sucintos livros sobre como escrever bem. Usar palavras simples. Frases curtas e objetivas. Entre tantas outras coisas. Apesar de ser um livro voltado para escrita em ingl&ecirc;s, boa parte de suas dicas podem ser aplicadas em outras l&iacute;nguas, como portugu&ecirc;s.
      </li>
      <li>
      O post <a href="https://goo.gl/s9AeLs">"The Six Best Tips From On Writing Well"</a>) sumariza as principais
      dicas do livro "On Writing Well".
      </li>
      
      <li>
      <a href="https://sivers.org/book/OnWritingWell">"As notas de Derek Sivers"</a>), do livro "On Writing Well",
      cont&eacute;m v&aacute;rias dicas para melhorar substancialmente a habilidade de escrita de qualquer pessoa.
      Tente seguir as dicas para obter resultados cada vez melhores.
      </li>
   </ul>
   </li>
   
   <li>
   A terceira sugestão é revisar o processo de escrita. 
   <br><br>
   <ul class="lista">

      <li>
      Fa&ccedil;a uma revis&atilde;o adequada do conteúdo do artigo antes de submeter. Pe&ccedil;a para seus 
      co-autores mais experientes em escrita revisar cuidadosamente a vers&atilde;o final. Alternativamente, 
      pe&ccedil;a para amigos ou profissionais em revis&atilde;o de texto dar um toque de qualidade ao seu texto. 
      </li>

      <li>Trocar a cor da fonte ou o meio (digital/papel) pode ajudar melhorar a concentração em trechos previamente revisados.
      </li>

      <li>
      Faça uma revisão adequada da forma do artigo. Nesta estapa, atente para os aspectos como uso da v&iacute;rgula, crase e concord&acirc;ncia nominal.
      Verifique tamb&eacute;m se as refer&ecirc;ncias a figuras, tabelas, equa&ccedil;&otilde;es, etc. est&atilde;o corretas, se cita&ccedil;&otilde;es
      bibliogr&aacute;ficas constam corretamente como tamb&eacute;m se seus conte&uacute;dos procedem ao referenciado no
      artigo. 
      </li>

      <li>
      Na prepara&ccedil;&atilde;o do seu artigo, use o formato exigido, conforme o <a href="http://www.sbc.org.br/documentos-da-sbc/category/169-templates-para-artigos-e-capitulos-de-livros">
      modelo da SBC aqui.</a>
      </li>   

      <li>Faça uma revisão fim a fim de uma versão impressa evitar problemas de impressão.
      </li>
   </ul>
   </li>
   </ul>
