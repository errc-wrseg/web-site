<h3>Submiss&atilde;o de Trabalhos</h3>
<p>
Ser&atilde;o aceitos trabalhos em duas modalidades: artigos
completos de no m&aacute;ximo 8 (oito) p&aacute;ginas e resumos
estendidos de no m&aacute;ximo 4 (quatro) p&aacute;ginas. O
n&uacute;mero m&aacute;ximo de p&aacute;ginas para ambas as
modalidades inclui resumo, abstract, figuras, refer&ecirc;ncias e
anexos. O texto pode ser escrito nos idiomas portugu&ecirc;s,
espanhol ou ingl&ecirc;s, seguindo os formatos (LaTeX e Word)
dispon&iacute;veis no site da SBC, atrav&eacute;s do
endere&ccedil;o <a href="http://www.sbc.org.br">http://www.sbc.org.br</a> (menu Documentos,
Publica&ccedil;&otilde;es/Templates para Artigos e
Cap&iacute;tulos de Livros). A submiss&atilde;o de trabalhos
ser&aacute; exclusivamente eletr&ocirc;nica.<br>
<br>
Em breve ser&atilde;o publicadas informa&ccedil;&otilde;es sobre o per&iacute;odo de
submiss&atilde;o e forma de envio.</p>

<h3>
Sugest&otilde;es aos Autores
</h3>
<p>
O objetivo desta p&aacute;gina &eacute; oferecer sugest&otilde;es aos autores que desejam submeter seus
artigos para publica&ccedil;&atilde;o e apresenta&ccedil;&atilde;o na ERRC/WRSeg 2019.  N&atilde;o se trata de um conte&uacute;do 
estruturado, nem uma "receita de bolo" para se produzir um artigo de qualidade, ou mesmo
ainda um artigo a ser aceito na ERRC/WRSeg. Consiste sim de uma s&eacute;rie de informa&ccedil;&otilde;es ou 
links que apontam para recursos que tratam do assunto.
   </p>
   <ul class="lista">
   <li>
O primeiro passo &eacute; analisar o formul&aacute;rio de avalia&ccedil;&atilde;o a ser empregado na ERRC/WRSeg.
Cada artigo ser&aacute; revisado por um grupo de no m&iacute;nimo 3 revisores. Tente voc&ecirc; mesmo avaliar
qu&atilde;o bem seu artigo atende a cada um dos aspectos. Verifique se o artigo exp&otilde;e claramente
cada um dos aspectos avaliados; por exemplo, no quesito originalidade, verifique se o seu
artigo compara o trabalho com outros na literatura; no quesito m&eacute;rito t&eacute;cnico, se o artigo
diz claramente ao revisor qual a contribui&ccedil;&atilde;o do trabalho.
</li><li>
Um dos melhores recursos dispon&iacute;veis em Portugu&ecirc;s &eacute; o "Pequeno Manual da Escrita T&eacute;cnica",
por Roberto Hexsel, da UFPR. Este material &eacute; uma boa leitura para alunos ao escreverem seus
trabalhos. Um dos lemas do texto &eacute; "leia o que escreveu";
</li><li>
"Writing Technical Articles" (<a href="http://www.cs.columbia.edu/~hgs/etc/writing-style.html">http://www.cs.columbia.edu/~hgs/etc/writing-style.html</a>),
escrito por Henning Schulzrinne, da Universidade de Columbia em Nova York, descreve em detalhes
o processo de se elaborar um artigo t&eacute;cnico de qualidade na &aacute;rea da computa&ccedil;&atilde;o, tratando tamb&eacute;m
aspectos de estilo (<a href="https://www.bartleby.com/141/index.html">https://www.bartleby.com/141/index.html</a>), apresenta&ccedil;&atilde;o dos dados, entre outros.
Trata-se de um material bastante interessante.  Salientamos a quest&atilde;o da Introdu&ccedil;&atilde;o
(<a href="http://www.cs.columbia.edu/~hgs/etc/intro-style.html">http://www.cs.columbia.edu/~hgs/etc/intro-style.html</a>), por ser algo bastante importante;
</li><li>
"Dicas sobre escrita" (<a href="http://www.ic.unicamp.br/~rodolfo/dicas/escrita.html">http://www.ic.unicamp.br/~rodolfo/dicas/escrita.html</a>) e "Dicas sobre
apresenta&ccedil;&otilde;es" (<a href="http://www.ic.unicamp.br/~rodolfo/dicas/apresentacoes.html">http://www.ic.unicamp.br/~rodolfo/dicas/apresentacoes.html</a>), p&aacute;gina elabora por
Rodolfo de Azevedo (UNICAMP), &eacute; bem sucinta e cont&eacute;m  dicas para escrita de um artigo 
(apresenta&ccedil;&atilde;o do mesmo, se aceito).
</li><li>
H&aacute; uma p&aacute;gina com dicas gramaticais aqui, e recomendo como material de refer&ecirc;ncia em rela&ccedil;&atilde;o ao 
Portugu&ecirc;s, consulte aqui o manual de escrita t&eacute;cnica, na PUCRS.
"On Writing Well", escrito por William Zinsser, &eacute; um dos melhores e mais sucintos livros sobre
como escrever bem. Usar palavras simples. Frases curtas e objetivas. Entre tantas outras coisas. 
Apesar de ser um livro voltado para escrita em ingl&ecirc;s, boa parte de suas dicas podem ser aplicadas 
em outras l&iacute;nguas, como portugu&ecirc;s.
</li><li>
O post The Six Best Tips From "On Writing Well" (<a href="https://goo.gl/s9AeLs">https://goo.gl/s9AeLs</a>) sumariza as principais
dicas do livro "On Writing Well".
</li><li>
As notas de Derek Sivers (<a href="https://sivers.org/book/OnWritingWell">https://sivers.org/book/OnWritingWell</a>), do livro "On Writing Well",
cont&eacute;m v&aacute;rias dicas para melhorar substancialmente a habilidade de escrita de qualquer pessoa.
Tente seguir as dicas para obter resultados cada vez melhores.
</li>
</ul>
<p>
Antes de submeter seu artigo via JEMS , fa&ccedil;a uma revis&atilde;o adequada do mesmo. Pe&ccedil;a para seus 
co-autores mais experientes em escrita revisar cuidadosamente a vers&atilde;o final. Alternativamente, 
pe&ccedil;a para amigos ou profissionais em revis&atilde;o de texto dar um toque de qualidade ao seu texto. 
Em rela&ccedil;&atilde;o &agrave; escrita, atente para os aspectos como uso da v&iacute;rgula, crase e concord&acirc;ncia nominal.
Verifique tamb&eacute;m se as refer&ecirc;ncias a figuras, tabelas, equa&ccedil;&otilde;es, etc. est&atilde;o corretas, se cita&ccedil;&otilde;es
bibliogr&aacute;ficas constam corretamente como tamb&eacute;m se seus conte&uacute;dos procedem ao referenciado no
artigo. Na prepara&ccedil;&atilde;o do seu artigo, use o formato exigido, conforme o <a href="http://www.sbc.org.br/documentos-da-sbc/category/169-templates-para-artigos-e-capitulos-de-livros">
modelo da SBC aqui.</a>
</p>
