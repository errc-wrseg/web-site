        <a class="anchor" id="oficina_nuvem"></a>
        <h3>Oficina 4: Virtualização através de containers para plataformas de computação em nuvem</h3>
        <p><b>Data:</b> 17/09/2019 <b>Horário:</b> 14:30-16:00, 16:30-18:00 <b>Local:</b> Laboratório 102</p>
        <p><b>Autores:</b> Raul Leiria, Adriano Vogel, Dalvan Griebler (PUCRS), Claudio Schepke (UNIPAMPA)</p>
        <p>
        <b>Resumo:</b> A computação em nuvem disponibiliza, sob demanda, recursos e sistemas para os usuários através de redes de computadores. Seus múltiplos modelos e abstrações permitem que o usuário escolha os recursos que mais se adequam às necessidades de suas aplicações, sem por isso perder à escalabilidade. A utilização de containers na computação em nuvem é tendência na indústria e na academia. Seu uso permite à implantação de sistemas computacionais com menores custos na utilização de recursos, se comparado com a utilização de máquinas virtuais. Em vista disso, o objetivo desta oficina é introduzir à utilização de containers Docker para computação em nuvem. A oficina tem enfoque pra ́tico ao instalar, criar e orquestrar containers em uma nuvem OpenStack. Espera-se que ao final da oficina o participante consiga configurar e operar uma nuvem privada que utilize virtualização através de containers. 
        </p> 
