    <h4>Alegrete</h4>
    <p>
    O município de Alegrete é situado na fronteira oeste do estado, a cerca de 500 Km de Porto Alegre, e possui uma população de aproximadamente 80.000 habitantes. A cidade apresenta uma variedade de belezas naturais (por exemplo, o Balneário Caverá e a Ponte de Pedra) e históricas (como a Ponte do Ibirapuitã, a Igreja Matriz, a Estação Ferroviária e o Museu do Gaúcho). A cidade é importante para a cultura gaúcha, sendo considerada uma das mais icônicas do RS. Em Alegrete ocorre anualmente no dia 20 de setembro, como parte das festividades comemorativas da semana Farroupilha, o maior desfile do Gaúcho do Estado.  Acesse uma matéria  produzida pela RBS TV sobre a região <a href="https://globoplay.globo.com/v/6361455/" alt="#PartiuRS Alegrete">aqui</a>.
    </p>
          
    <h4>Unipampa</h4>
    <p>
      Desde 2006, Alegrete abriga um campus da Unipampa. Atualmente, o campus oferece 7 cursos de
      gradua&ccedil;&atilde;o (Ci&ecirc;ncia da
      Computa&ccedil;&atilde;o, Engenharia de Software, Engenharia de
      Telecomunica&ccedil;&otilde;es, Engenharia Civil, Engenharia
      Agr&iacute;cola, Engenharia El&eacute;trica e Engenharia
      Mec&acirc;nica) e 4 cursos de pós-graduação (Mestrado em Engenharia de Software, Mestrado em Engenharia Elétrica, Mestrado em Engenharias e Especialização em Engenharia Econômica).
    </p>
	
