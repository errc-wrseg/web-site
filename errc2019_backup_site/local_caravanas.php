        <h4>O que é considerado uma caravana?</h4>
        
        <p>Um grupo com pelo menos 8 integrantes que chegue ao evento através de um mesmo meio de transporte (kombi, van, micro-ônibus, ônibus, etc.).
        </p>

        <h4>Quais são os benefícios do Programa de Caravanas?</h4>
        <ul>
        <li>A cada 8 integrantes: 01 ingresso</li>
        <li>A cada 16 integrantes: 01 camiseta personalizada</li>
        </ul>
        
        <h4>Cronograma</h4>
